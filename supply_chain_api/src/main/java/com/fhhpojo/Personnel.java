package com.fhhpojo;

public class Personnel {
 
	private int per_id;
	private int pos_id;
	private String per_name;
	private String per_phone;
	private String per_number;
	public int getPer_id() {
		return per_id;
	}
	public void setPer_id(int per_id) {
		this.per_id = per_id;
	}
	public int getPos_id() {
		return pos_id;
	}
	public void setPos_id(int pos_id) {
		this.pos_id = pos_id;
	}
	public String getPer_name() {
		return per_name;
	}
	public void setPer_name(String per_name) {
		this.per_name = per_name;
	}
	public String getPer_phone() {
		return per_phone;
	}
	public void setPer_phone(String per_phone) {
		this.per_phone = per_phone;
	}
	public String getPer_number() {
		return per_number;
	}
	public void setPer_number(String per_number) {
		this.per_number = per_number;
	}
	
	
}
