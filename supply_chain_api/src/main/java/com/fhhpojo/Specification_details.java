package com.fhhpojo;

public class Specification_details {

	private int s_id;
	private int d_id;
	private String production_business;
	private String mode;
	public int getS_id() {
		return s_id;
	}
	public void setS_id(int s_id) {
		this.s_id = s_id;
	}
	public int getD_id() {
		return d_id;
	}
	public void setD_id(int d_id) {
		this.d_id = d_id;
	}
	public String getProduction_business() {
		return production_business;
	}
	public void setProduction_business(String production_business) {
		this.production_business = production_business;
	}
	public String getMode() {
		return mode;
	}
	public void setMode(String mode) {
		this.mode = mode;
	}
	
	
}
